# Math Module Methods Example
import math

# for the value of pi 
print("the value of pi is:", math.pi)


# for the value of e 
print("the value of e is:", math.e)

# Square Root
print("Square root of 16:", math.sqrt(16))  # sqrt(x) -> returns square root

# Power
print("2 raised to power 3:", math.pow(2, 3))  # pow(x, y) -> x^y

# Ceiling
print("Ceiling of 4.2:", math.ceil(4.2))  # ceil(x) -> rounds up

# Floor
print("Floor of 4.8:", math.floor(4.8))  # floor(x) -> rounds down

# Factorial
print("Factorial of 5:", math.factorial(5))  # factorial(x) -> 5*4*3*2*1

# Absolute Value
print("Absolute value of -7:", math.fabs(-7))  # fabs(x) -> positive value

# Logarithm
print("Log base 2 of 8:", math.log(8, 2))  # log(x, base) -> logarithm
