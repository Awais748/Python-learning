# PYTHON DATA TYPES



# 1. INTEGER (int)
# Whole numbers without decimals

age = 20
year = 2026

print("Integer Example:", age)
print(type(age))


# 2. FLOAT (float)
# Numbers with decimal points

price = 99.99
temperature = 36.5

print("Float Example:", price)
print(type(price))


# 3. COMPLEX (complex)
# Numbers with real and imaginary parts

complex_number = 3 + 4j

print("Complex Example:", complex_number)
print(type(complex_number))


# 4. STRING (str)
# Used to store text

name = "Awais"
language = "Python"

print("String Example:", name)
print(type(name))


# 5. BOOLEAN (bool)
# Only True or False values

is_student = True
is_admin = False

print("Boolean Example:", is_student)
print(type(is_student))


# 6. LIST
# Ordered and changeable collection

fruits = ["apple", "banana", "mango"]

print("List Example:", fruits)
print(type(fruits))


# 7. TUPLE
# Ordered but not changeable

coordinates = (10, 20)

print("Tuple Example:", coordinates)
print(type(coordinates))


# 8. SET
# Unordered collection of unique values

numbers = {1, 2, 3, 4}

print("Set Example:", numbers)
print(type(numbers))


# 9. DICTIONARY
# Stores data in key-value pairs

student = {
    "name": "Awais",
    "age": 20,
    "city": "Lahore"
}

print("Dictionary Example:", student)
print(type(student))# Day 1 - Data Types
